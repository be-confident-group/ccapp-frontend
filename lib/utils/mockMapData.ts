function generateRandomRoute(
  center: Coordinate,
  points: number,
  variance: number
): Coordinate[] {
  const route: Coordinate[] = [center];

  for (let i = 1; i < points; i++) {
    const prev = route[i - 1];
    route.push({
      latitude: prev.latitude + (Math.random() - 0.5) * variance,
      longitude: prev.longitude + (Math.random() - 0.5) * variance,
    });
  }

  return route;
}

